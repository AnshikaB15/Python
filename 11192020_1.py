"""
    Example of creating our own class.
        And, creating objects from that class.

    In this example, the class is empty (does not hold any data or behavior).
"""

class Student:
    pass
    
niti = Student()        # creates an object of Student class, and stores in 'niti' variable
print(type(niti))
print(niti)

print()
print()

rohan = Student()
print(type(rohan))
print(rohan)            # creates an object of Student class, and stores in 'rohan' variable





