

# Examples of "for" loop.

# This prints numbers from 3 to 7
for no in range(3, 8):
    print(no)

print()

# This prints numbers from 0 to 5
for no in range(6):
    print(no)

print()

# This prints numbers from 10 to 6 in reverse
for no in range(10, 5, -1):
    print(no)

print()

# This prints odd numbers between 1 to 10
for no in range(1, 10, 2):
    print(no)

print()

# This prints each and every character present in your name
for no in "Python":
    print(no)
